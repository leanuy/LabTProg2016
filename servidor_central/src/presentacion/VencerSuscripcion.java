    private void costumerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_costumerActionPerformed
        cliente = String.valueOf(costumer.getSelectedItem());
        try {
            DataSuscripcionVigente dataSusc = (DataSuscripcionVigente)interf.getSuscripcionDeCliente(cliente);
            String suscripcion = "Estado: "
                    + dataSusc.getEstado().toString()
                    + " updated: "
                    + dataSusc.getFechaDesdeStr()
                    + " tipo: "
                    + dataSusc.getTipo().toString();
            if (dataSusc.getEstado()==EstadoSuscripcion.VIGENTE) {
                vencer.setEnabled(true);
            }
            datasusc.setText(suscripcion);
        } catch (ClienteInexistenteException ex) {
            JOptionPane.showMessageDialog(this, "El cliente no existe.", "ERROR", JOptionPane.ERROR_MESSAGE);
        } catch (NoHaySuscripcionException ex) {
            datasusc.setText("El cliente no tiene suscripciones");
        }
    }//GEN-LAST:event_costumerActionPerformed


